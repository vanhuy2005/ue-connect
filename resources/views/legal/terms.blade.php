<p>Chào mừng bạn đến với <strong>UEConnect</strong>. Bằng việc truy cập và sử dụng nền tảng của chúng tôi, bạn đồng ý tuân thủ các Điều khoản sử dụng dưới đây. Vui lòng đọc kỹ trước khi bắt đầu hành trình của bạn tại cộng đồng sinh viên Trường Đại học Sư phạm TP.HCM (HCMUE).</p>

<h2>1. Giới thiệu về UEConnect</h2>
<p>UEConnect là mạng xã hội và nền tảng kết nối dành riêng cho sinh viên, cựu sinh viên và giảng viên của Trường Đại học Sư phạm TP.HCM. Mục tiêu của chúng tôi là tạo ra một không gian trực tuyến an toàn, thân thiện để chia sẻ kiến thức, tìm kiếm cơ hội (việc làm, nghiên cứu) và kết nối thông qua hệ thống Cố vấn (Mentor - Mentee).</p>

<h2>2. Điều kiện tham gia</h2>
<ul>
    <li>Bạn phải là sinh viên, cựu sinh viên hoặc giảng viên/nhân viên hiện đang công tác tại HCMUE.</li>
    <li>Bạn phải sử dụng địa chỉ email do trường cấp (đuôi <code>@hcmue.edu.vn</code> hoặc <code>@student.hcmue.edu.vn</code>) hoặc cung cấp bằng chứng định danh (Thẻ sinh viên, CCCD) để hệ thống xác thực.</li>
    <li>Mỗi cá nhân chỉ được phép tạo và sử dụng <strong>một (01) tài khoản duy nhất</strong> trên UEConnect.</li>
</ul>

<h2>3. Quyền và Nghĩa vụ của Người dùng</h2>
<h3>3.1. Nghĩa vụ bảo mật</h3>
<p>Bạn chịu trách nhiệm hoàn toàn về việc bảo mật thông tin đăng nhập của mình. Bất kỳ hoạt động nào diễn ra dưới tài khoản của bạn sẽ được xem là do bạn thực hiện. Nếu phát hiện truy cập trái phép, bạn cần báo cáo ngay cho Ban Quản Trị.</p>

<h3>3.2. Cung cấp thông tin chính xác</h3>
<p>Bạn cam kết những thông tin cung cấp trong quá trình đăng ký và xác thực tài khoản là hoàn toàn chính xác. Việc sử dụng thông tin giả mạo sẽ dẫn đến việc tài khoản bị khóa vĩnh viễn.</p>

<h3>3.3. Tôn trọng bản quyền và Sở hữu trí tuệ</h3>
<p>Bạn không được phép đăng tải, chia sẻ các nội dung vi phạm bản quyền hoặc quyền sở hữu trí tuệ của người khác. Đặc biệt, việc chia sẻ trái phép các tài liệu thi, đề thi bảo mật của Nhà trường là hành vi bị nghiêm cấm.</p>

<h2>4. Quy định đối với hệ thống Mentor - Mentee</h2>
<p>UEConnect cung cấp tính năng kết nối Cố vấn. Khi tham gia với tư cách Mentor hoặc Mentee, bạn đồng ý:</p>
<ul>
    <li>Giao tiếp dựa trên sự tôn trọng, chuyên nghiệp và vì mục tiêu học thuật/phát triển nghề nghiệp.</li>
    <li>Không sử dụng tính năng Mentor để quấy rối, gạ gẫm hoặc thực hiện các hành vi không phù hợp với chuẩn mực sư phạm.</li>
    <li>UEConnect không chịu trách nhiệm về các thỏa thuận cá nhân (bao gồm cả tài chính nếu có) giữa Mentor và Mentee diễn ra bên ngoài nền tảng.</li>
</ul>

<h2>5. Quyền của UEConnect</h2>
<ul>
    <li><strong>Quản lý nội dung:</strong> Chúng tôi có quyền (nhưng không có nghĩa vụ) rà soát, xóa hoặc ẩn bất kỳ nội dung nào vi phạm Tiêu chuẩn cộng đồng mà không cần báo trước.</li>
    <li><strong>Chấm dứt dịch vụ:</strong> UEConnect bảo lưu quyền tạm ngưng hoặc khóa vĩnh viễn tài khoản của người dùng vi phạm Điều khoản sử dụng này hoặc có hành vi gây tổn hại đến cộng đồng HCMUE.</li>
    <li><strong>Thay đổi nền tảng:</strong> Chúng tôi có quyền nâng cấp, sửa đổi hoặc ngừng cung cấp một phần hoặc toàn bộ dịch vụ bất cứ lúc nào để bảo trì hoặc phát triển.</li>
</ul>

<h2>6. Giới hạn trách nhiệm</h2>
<p>UEConnect được cung cấp "nguyên trạng". Mặc dù chúng tôi cố gắng hết sức để duy trì tính ổn định và an toàn của hệ thống, chúng tôi không đảm bảo rằng dịch vụ sẽ không bị gián đoạn, không có lỗi hoặc hoàn toàn an toàn trước các cuộc tấn công mạng. UEConnect từ chối mọi trách nhiệm liên quan đến thiệt hại phát sinh từ việc bạn sử dụng nền tảng (bao gồm cả việc mất dữ liệu).</p>

<h2>7. Sửa đổi Điều khoản</h2>
<p>Chúng tôi có thể cập nhật Điều khoản này theo thời gian. Nếu có thay đổi quan trọng, chúng tôi sẽ thông báo cho bạn thông qua hệ thống hoặc qua email. Việc bạn tiếp tục sử dụng dịch vụ sau khi các sửa đổi có hiệu lực đồng nghĩa với việc bạn chấp nhận Điều khoản mới.</p>

<h2>8. Liên hệ</h2>
<p>Nếu bạn có bất kỳ câu hỏi nào về Điều khoản này, vui lòng liên hệ với chúng tôi qua email hỗ trợ: <strong>support@ueconnect.edu.vn</strong>.</p>
